import turtle
# head
turtle.forward(50)
turtle.left(90)
turtle.forward(50)
turtle.left(90)
turtle.forward(50)
turtle.left(90)
turtle.forward(50)
turtle.left(90)
# move to neck
turtle.penup()
turtle.forward(25)
turtle.pendown()
# neck
turtle.right(90)

# the end
turtle.done()
